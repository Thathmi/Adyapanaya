    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        char passwordArray[] = jPasswordField1.getPassword();
        String password = String.valueOf(passwordArray);

        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "please enter email", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "please enter password", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            String email = jTextField1.getText();

            try {
                ResultSet resultset = MySQL.search("SELECT * FROM `admin` WHERE `email`='" + email + "' AND `password`='" + password + "'");

                if (resultset.next()) {

                    userId = Integer.parseInt(resultset.getString("id"));
                    
                      Home home = new Home();
                     
                    home.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid Email or Password", "warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

